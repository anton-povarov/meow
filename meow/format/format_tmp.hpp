#ifndef MEOW_FORMAT__FORMAT_TMP_HPP_
#define MEOW_FORMAT__FORMAT_TMP_HPP_

#include "format.hpp"
#include "sink/char_buffer.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////
namespace meow { namespace format {
////////////////////////////////////////////////////////////////////////////////////////////////

	template<size_t N, class F, class... A>
	inline stack_buffer_t<N> fmt_tmp(F const& fmt_str, A const&... args)
	{
		stack_buffer_sink_t<N> sink;
		return fmt(sink, fmt_str, args...);
	}

	template<size_t N, class... A>
	inline str_ref write_tmp(A const&... args)
	{
		stack_buffer_sink_t<N> sink;
		return write(sink, args...);
	}

////////////////////////////////////////////////////////////////////////////////////////////////
}} // namespace meow { namespace format {
////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW_FORMAT__FORMAT_TMP_HPP_

