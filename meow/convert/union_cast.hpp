#ifndef MEOW_CONVERT__UNON_CAST_HPP_
#define MEOW_CONVERT__UNON_CAST_HPP_

////////////////////////////////////////////////////////////////////////////////////////////////
namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

	template<class To, class From>
	union union_caster_t {
		To to;
		From from;
	};

	template<class To, class From>
	inline To union_cast(From const& from)
	{
		union_caster_t<To, From> cast;
		cast.from = from;
		return cast.to;
	}

////////////////////////////////////////////////////////////////////////////////////////////////
} // namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW_CONVERT__UNON_CAST_HPP_

