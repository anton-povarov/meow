#ifndef MEOW_MAPPING__HTTP_HEADERS_HPP_
#define MEOW_MAPPING__HTTP_HEADERS_HPP_

// TODO: add a usage example

#include <meow/str_ref.hpp>

////////////////////////////////////////////////////////////////////////////////////////////////
namespace meow { namespace mapping {
////////////////////////////////////////////////////////////////////////////////////////////////

	struct http_version_t
	{
		unsigned int major;
		unsigned int minor;
	};

	typedef int http_status_t;

	struct http_request_line_t
	{
		http_version_t 	version;
		str_ref 		method;
		str_ref 		uri;
	};

	struct http_response_line_t
	{
		http_version_t 	version;
		http_status_t 	status;
		str_ref 		message;
	};

	struct http_header_kvref_t
	{
		str_ref name;
		str_ref value;
	};

////////////////////////////////////////////////////////////////////////////////////////////////
}} // namespace meow { namespace mapping {
////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW_MAPPING__HTTP_HEADERS_HPP_

