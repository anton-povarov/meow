#ifndef MEOW_UTILITY__BITMASK_HPP_
#define MEOW_UTILITY__BITMASK_HPP_

////////////////////////////////////////////////////////////////////////////////////////////////

template<class T1, class T2>
inline T1 bitmask_set(T1& mask, T2 bit)
{
	return mask |= bit;
}

template<class T1, class T2>
inline T1 bitmask_clear(T1& mask, T2 bit)
{
	return mask &= ~bit;
}

template<class T1, class T2>
inline T1 bitmask_test(T1 const& mask, T2 bit)
{
	return (mask & bit);
}

////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW_UTILITY__BITMASK_HPP_

