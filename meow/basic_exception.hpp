#ifndef MEOW__BASIC_EXCEPTION_HPP_
#define MEOW__BASIC_EXCEPTION_HPP_

#include <exception>

////////////////////////////////////////////////////////////////////////////////////////////////
namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

	struct basic_exception : virtual public std::exception
	{
		basic_exception(char const *e) : e_(e) {}
		virtual ~basic_exception() throw() {}
		virtual char const* what() const throw() { return e_; }

	private:
		char const *e_;
	};

	template<class Tag>
	struct tagged_exception : virtual public basic_exception
	{
		tagged_exception(char const *e) : basic_exception(e) {}
	};

////////////////////////////////////////////////////////////////////////////////////////////////
} // namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW__BASIC_EXCEPTION_HPP_



