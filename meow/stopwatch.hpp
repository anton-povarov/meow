#ifndef MEOW__STOPWATCH_HPP_
#define MEOW__STOPWATCH_HPP_

#include <meow/unix/time.hpp>

////////////////////////////////////////////////////////////////////////////////////////////////
namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

	struct stopwatch_t
	{
		os_timeval_t start_tv;

		stopwatch_t(bool start = true)
		{
			if (start)
				reset();
		}

		void reset()
		{
			start_tv = now();
		}

		os_timeval_t now() const
		{
			return os_unix::gettimeofday_ex();
		}

		os_timeval_t stamp() const
		{
			return now() - start_tv;
		}
	};

////////////////////////////////////////////////////////////////////////////////////////////////
} // namespace meow {
////////////////////////////////////////////////////////////////////////////////////////////////

#endif // MEOW__STOPWATCH_HPP_

